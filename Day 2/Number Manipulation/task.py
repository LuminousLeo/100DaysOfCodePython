bmi = 84 / 1.65 ** 2
#float value of bmi
print(bmi)
#truncating/flooring bmi value
print(int(bmi))
#rounding up bmi
print(round(bmi))
#rounding down a number
print(round(2.3455))
#round bmi up to 2 decimal places
print(round(bmi, 2))

#assignment operators
score = 0
#add 1 to score
score += 1
#subtract 1 to score
score -= 1
#divide score by 1
score /= 1
#multiply score by 1
score *= 1

#f-strings
print(f'My bmi is {bmi}')